
public class UnaryOperators {

	public static void main(String[] args) {
	
		int a=10;
		System.out.println(a);//10
		a++;//11 postincrement
		System.out.println(a);//11
		++a;//12 pre increment
		System.out.println(a); //12
		--a;//11 pre decrement
		System.out.println(a);//  11
		a--; //10
		System.out.println(a);//10

	}

}
